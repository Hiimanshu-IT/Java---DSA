public class Stringmethods {
    public static void main(String[] args) {
        String name = "Himanshu";
      /*  1. length
          System.out.println(name.length());*/

      /*  
          2.to upper case
          System.out.println(name.toUpperCase()); */

      /*   3.to lower case
          System.out.println(name.toLowerCase()); */

      /*
          4. trim 
          System.out.println(name.trim());*/

      /*
          5. substring index
          System.out.println(name.substring(5)); */

      /*  6.substring(start index,end index) 
          System.out.println(name.substring(2,5)); */

      /*  7. replace character
          System.out.println(name.replace('i' ,'a' )); */

      /*  8. replace string
          System.out.println(name.replace("Him","org"));*/

      /*  9. starts with 
          System.out.println(name.startsWith("Him")); */

      /*  10. ends with
          System.out.println(name.endsWith("shu"));*/

      /*  11. char at   
          System.out.println(name.charAt(2));*/

      /*  12. index Of 
          System.out.println(name.indexOf('i'));*/

     /*   13. last index of  
          System.out.println(name.lastIndexOf("shu",5));*/

     /*   14. equals 
          System.out.println(name.equals("Himanshu")); */

     /*   15. equals Ignore the case  
          System.out.println(name.equalsIgnoreCase("HImanshU")); */
           
    }
}
